async function testStudyPlanAPI() {
  console.log('🧪 Testing Study Plan API Fix...\n');

  try {
    // Test the study plan API with university-elective-1
    console.log('1. Testing study plan API with university-elective-1...');
    const response = await fetch('http://localhost:3000/api/study-plan?quizId=university-elective-1&score=0&total=5&useGemini=false', {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
      }
    });

    console.log(`Response status: ${response.status}`);
    
    if (response.ok) {
      const data = await response.json();
      console.log('✅ Success! Study plan generated');
      console.log(`   Course: ${data.courseTitle || 'Unknown'}`);
      console.log(`   Current Level: ${data.currentLevel || 'Unknown'}`);
      console.log(`   Target Score: ${data.targetScore || 'Unknown'}%`);
      console.log(`   Program ID: ${data.programId || 'Unknown'}`);
      console.log(`   Study Steps: ${data.studySteps?.length || 0} steps`);
      console.log(`   Generated By: ${data.generatedBy || 'Unknown'}`);
      console.log(`   Enhanced: ${data.enhanced ? 'Yes' : 'No'}`);
    } else {
      const errorData = await response.json().catch(() => ({}));
      console.log(`❌ Error: ${response.status} - ${errorData.error || response.statusText}`);
    }
  } catch (error) {
    console.log(`❌ Error testing study plan API: ${error.message}`);
    console.log(`Error stack: ${error.stack}`);
  }

  console.log('\n🎉 Study plan API testing completed!');
}

testStudyPlanAPI(); 